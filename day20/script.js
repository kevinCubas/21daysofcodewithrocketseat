let timeOut;

const displayEmojis = () => {
  const emojisElements = EMOJIS.map(emoji => `<div class="emoji"><i>${emoji}</i></div>`).join("");

  EMOJI_CONTAINER.innerHTML = emojisElements;

  document.querySelectorAll(".emoji").forEach(emoji => {
    emoji.addEventListener("click", (e) => copyToClipboard(e.target.innerHTML) )
  })

};

function copyToClipboard(emoticon) {
  clearTimeout(timeOut)
  navigator.clipboard.writeText(emoticon)
  COPIED_TEXT.classList.add("show-copied");
  timeOut = setTimeout(() => {
    COPIED_TEXT.classList.remove("show-copied");
  }, 1000)
};

displayEmojis();